document.addEventListener('DOMContentLoaded', () => {

  const langButtons = document.querySelectorAll('.lang-btn');
  const savedLang = localStorage.getItem('site-language') || 'zh';

  function applyLanguage(lang) {
    document.documentElement.lang = lang === 'en' ? 'en' : 'zh-CN';

    const nodes = document.querySelectorAll('[data-i18n]');

    nodes.forEach(node => {
      const key = node.getAttribute('data-i18n');
      if (translations[lang] && translations[lang][key]) {
        node.textContent = translations[lang][key];
      }
    });

    langButtons.forEach(button => {
      button.classList.toggle('active', button.dataset.lang === lang);
    });

    localStorage.setItem('site-language', lang);
  }

  // ✅ 👉 这一段就在这里（必须在里面）
  langButtons.forEach(button => {
    button.addEventListener('click', () => {
      applyLanguage(button.dataset.lang);
    });
  });

  applyLanguage(savedLang);

});



const observer = new IntersectionObserver(
  entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
      }
    });
  },
  { threshold: 0.16 }
);

document.querySelectorAll('.reveal').forEach(item => observer.observe(item));

const sideNavItems = document.querySelectorAll('.side-nav-item');

function setActiveSideNav(id) {
  sideNavItems.forEach(item => {
    item.classList.toggle('active', item.dataset.section === id);
  });
}

const sectionMap = [
  { id: 'top', el: document.querySelector('.hero-immersive') },
  { id: 'about', el: document.getElementById('about') },
  { id: 'products', el: document.getElementById('products') },
  { id: 'facility', el: document.getElementById('facility') },
  { id: 'contact', el: document.getElementById('contact') }
];

const sectionObserver = new IntersectionObserver(
  entries => {
    const visibleEntries = entries
      .filter(entry => entry.isIntersecting)
      .sort((a, b) => b.intersectionRatio - a.intersectionRatio);

    if (visibleEntries.length > 0) {
      const currentId = visibleEntries[0].target.dataset.sideSection;
      setActiveSideNav(currentId);
    }
  },
  {
    threshold: [0.2, 0.35, 0.5, 0.65],
    rootMargin: '-20% 0px -20% 0px'
  }
);

sectionMap.forEach(section => {
  if (section.el) {
    section.el.dataset.sideSection = section.id;
    sectionObserver.observe(section.el);
  }
});

sideNavItems.forEach(item => {
  item.addEventListener('click', () => {
    setActiveSideNav(item.dataset.section);
  });
});

